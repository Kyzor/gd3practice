class Powers {

    public static void main(String args[]){
        double n = 3.5;

        System.out.println("The number = " + n);
        System.out.println(n + " sqared = " + n * n);
        System.out.println(n + " cubed = " + n * n * n);
        System.out.println(n + " to the fourth = " + Math.pow(n, 4));
    }
}
