class TotalHeight {

    public static void main(String args[]){
        int feet = 5;
        double inches = 2.5;
        double totalInches = 62.5;

        System.out.println("Your height (the feet part) is " + feet);
        System.out.println("and the inches part is " + inches);
        System.out.println("Your total height is " + totalInches + " inches.");
        System.out.println("Your total height in centimeters is " + (totalInches * 2.54));
    }
}
